export function cloneLead(lead: AdminLead): AdminLead {
  return {
    ...lead,
    tags: lead.tags.map((t) => ({ ...t })),
    activities: lead.activities.map((a) => ({ ...a })),
    checklist: lead.checklist.map((c) => ({ ...c })),
    nearbyLeads: lead.nearbyLeads.map((n) => ({ ...n })),
    quoteSummary: lead.quoteSummary ? { ...lead.quoteSummary } : undefined,
  };
}

