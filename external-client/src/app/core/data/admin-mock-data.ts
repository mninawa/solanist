function inboxLead(
  id: string,
  name: string,
  snippet: string,
  location: string,
  city: string,
  postcode: string,
  status: AdminLead['status'],
  urgency: AdminLead['urgency'],
  createdAt: string,
  score: number,
): AdminLead {
  const firstName = name.split(' ')[0];
  return {
    id,
    source: 'bark_email',
    status,
    pipelineStage:
      status === 'new'
        ? 'new'
        : status === 'contacted'
          ? 'contacted'
          : status === 'interested'
            ? 'interested'
            : status === 'quote_sent'
              ? 'quote_sent'
              : 'new',
    customerName: name,
    customerEmail: `${firstName.toLowerCase()}@email.com`,
    customerPhone: '082 000 0000',
    propertyAddress: location,
    city,
    postcode,
    province: 'Gauteng',
    panelCount: 16,
    estimatedPanelsRange: '16–20',
    roofType: 'Tile',
    notes: snippet,
    requestSnippet: snippet,
    createdAt,
    urgency,
    leadScore: score,
    serviceType: 'Solar Panel Cleaning',
    tags: [{ label: 'Residential', tone: 'teal' }],
    activities: [
      {
        id: `${id}-act`,
        type: 'created',
        title: 'New lead created',
        description: 'Bark Email',
        timestamp: createdAt,
      },
    ],
    checklist: [],
    nearbyLeads: NEARBY_LEADS,
  };
}

